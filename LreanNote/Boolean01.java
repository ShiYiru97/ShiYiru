//布尔
public class Boolean01 {
	public static void main(String[] args) {
		//演示判断成绩是否通过的案例
		//首先定义一个布尔变量
		boolean isPass = true;
		if (isPass == true){
			System.out.println("考试通过，恭喜~");
		}else{
			System.out.println("考试没有通过，下次努力！");
		}
		boolean isPass02 = false;
		if (isPass02 == true){
			System.out.println("考试通过，恭喜~");
		}else{
			System.out.println("考试没有通过，下次努力！");
		}

	}

}